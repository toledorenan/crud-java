    private void cadastrar(){
        
        
        try {
            ProdutoVO pVO = new ProdutoVO();
            pVO.setNome(jtNome.getText());
            pVO.setValorCusto(Double.parseDouble(Conversao.converterVirgulaPonto(jtValorCusto.getText())));
            pVO.setQuantidade(Integer.parseInt(jtQuantidade.getText()));
            pVO.setFornecedor(jtFornecedor.getText());
            pVO.setCategoria((String) jcbCat.getSelectedItem());
            pVO.setPeso(Double.parseDouble(Conversao.converterVirgulaPonto(jtPeso.getText())));
            
            
            
            ProdutoServicos ps = servicos.ServicosFactory.getProdutoServicos();
            ps.cadastrarProduto(pVO);
            
            JOptionPane.showMessageDialog(rootPane, "Produto Cadastrado com sucesso!");
            limpar();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Erro!"+e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            
        }
    }    
    
    private void limpar(){
        jtNome.setText(null);
        jtValorCusto.setText(null);
        jtQuantidade.setText(null);
        jtFornecedor.setText(null);
        jtPeso.setText(null);
    }
